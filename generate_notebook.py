# ─── Cell 9: ResNet Training ──────────────────────────────────────────────────
c9 = code([
"import torch.nn as nn\n",
"import torch.optim as optim\n",
"from torchvision.models import mobilenet_v3_small\n",
"\n",
"class TorchCropDataset(Dataset):\n",
"    def __init__(self, base_ds, transform=None):\n",
"        self.base_ds = base_ds\n",
"        self.transform = transform\n",
"        \n",
"    def __len__(self): return len(self.base_ds)\n",
"    def __getitem__(self, idx):\n",
"        img, label = self.base_ds[idx]\n",
"        rgb = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)\n",
"        if self.transform:\n",
"            rgb = self.transform(rgb)\n",
"        return rgb, torch.tensor(label, dtype=torch.long)\n",
"\n",
"def train_head(head_name):\n",
"    print(f\"\\n{'='*50}\\nTraining {head_name.upper()} Head classifier\\n{'='*50}\")\n",
"    train_ds = PoseROIBuilder(DATASET_ROOT, split='train', roi_type=head_name)\n",
"    val_ds = PoseROIBuilder(DATASET_ROOT, split='val', roi_type=head_name)\n",
"    \n",
"    if len(train_ds) == 0:\n",
"        print(f\"No samples for {head_name}, skipping.\")\n",
"        return\n",
"\n",
"    transform = transforms.Compose([\n",
"        transforms.ToPILImage(),\n",
"        transforms.Resize((224, 224)),\n",
"        transforms.RandomHorizontalFlip(),\n",
"        transforms.ColorJitter(brightness=0.3, contrast=0.3, saturation=0.2),\n",
"        transforms.ToTensor(),\n",
"        transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225])\n",
"    ])\n",
"    \n",
"    val_transform = transforms.Compose([\n",
"        transforms.ToPILImage(),\n",
"        transforms.Resize((224, 224)),\n",
"        transforms.ToTensor(),\n",
"        transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225])\n",
"    ])\n",
"    \n",
"    train_loader = DataLoader(TorchCropDataset(train_ds, transform), batch_size=32, shuffle=True)\n",
"    val_loader = DataLoader(TorchCropDataset(val_ds, val_transform), batch_size=32, shuffle=False)\n",
"    \n",
"    device = 'cuda' if torch.cuda.is_available() else 'cpu'\n",
"    model = mobilenet_v3_small(weights='IMAGENET1K_V1')\n",
"    in_features = model.classifier[3].in_features\n",
"    model.classifier[3] = nn.Linear(in_features, 3)\n",
"    model = model.to(device)\n",
"    \n",
"    optimizer = optim.AdamW(model.parameters(), lr=1e-4, weight_decay=1e-4)\n",
"    scheduler = optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=30)\n",
"    criterion = nn.CrossEntropyLoss()\n",
"    \n",
"    best_val_acc = 0.0\n",
"    epochs = 30\n",
"    \n",
"    for epoch in range(1, epochs + 1):\n",
"        model.train()\n",
"        total_loss = 0.0\n",
"        for imgs, labels in train_loader:\n",
"            imgs, labels = imgs.to(device), labels.to(device)\n",
"            optimizer.zero_grad()\n",
"            out = model(imgs)\n",
"            loss = criterion(out, labels)\n",
"            loss.backward()\n",
"            optimizer.step()\n",
"            total_loss += loss.item()\n",
"            \n",
"        scheduler.step()\n",
"        \n",
"        model.eval()\n",
"        correct, total = 0, 0\n",
"        with torch.no_grad():\n",
"            for imgs, labels in val_loader:\n",
"                imgs, labels = imgs.to(device), labels.to(device)\n",
"                preds = model(imgs).argmax(dim=1)\n",
"                correct += (preds == labels).sum().item()\n",
"                total += len(labels)\n",
"                \n",
"        val_acc = correct / total if total > 0 else 0.0\n",
"        avg_loss = total_loss / len(train_loader)\n",
"        \n",
"        if epoch % 5 == 0 or epoch == 1:\n",
"            print(f\"  Epoch {epoch:2d}/{epochs} | Loss: {avg_loss:.4f} | Val Acc: {val_acc:.4f}\")\n",
"            \n",
"        if val_acc > best_val_acc:\n",
"            best_val_acc = val_acc\n",
"            torch.save(model.state_dict(), MODEL_DIR / f\"{head_name}_head.pth\")\n",
"            \n",
"    print(f\"Completed {head_name.upper()}. Best Val Acc: {best_val_acc:.4f}\")\n",
"\n",
"# Run training for all PPE class heads\n",
"for head in ['helmet', 'vest', 'gloves', 'boots', 'goggles']:\n",
"    train_head(head)\n",
"\n",
"print(\"\\nAll Training Phases Completed Successfully!\")"
])

