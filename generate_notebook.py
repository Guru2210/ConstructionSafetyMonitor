# ─── Cell 7: Pose Extractor ───────────────────────────────────────────────────
c7 = code([
"import cv2\n",
"import numpy as np\n",
"import torch\n",
"from torch.utils.data import Dataset, DataLoader\n",
"from torchvision import transforms\n",
"\n",
"class BBox:\n",
"    def __init__(self, x1, y1, x2, y2):\n",
"        self.x1, self.y1, self.x2, self.y2 = x1, y1, x2, y2\n",
"        \n",
"def compute_iou(boxA, boxB):\n",
"    xA = max(boxA.x1, boxB.x1)\n",
"    yA = max(boxA.y1, boxB.y1)\n",
"    xB = min(boxA.x2, boxB.x2)\n",
"    yB = min(boxA.y2, boxB.y2)\n",
"    interArea = max(0, xB - xA) * max(0, yB - yA)\n",
"    if interArea == 0: return 0.0\n",
"    boxAArea = (boxA.x2 - boxA.x1) * (boxA.y2 - boxA.y1)\n",
"    boxBArea = (boxB.x2 - boxB.x1) * (boxB.y2 - boxB.y1)\n",
"    return interArea / float(boxAArea + boxBArea - interArea)\n",
"\n",
"class PoseROIBuilder:\n",
"    def __init__(self, dataset_dir, split='train', roi_type='helmet'):\n",
"        self.dataset_dir = Path(dataset_dir)\n",
"        self.roi_type = roi_type\n",
"        self.split = split\n",
"        self.pose_model = YOLO('yolov8n-pose.pt')\n",
"        self.samples = []\n",
"        self._build()\n",
"        \n",
"    def _build(self):\n",
"        print(f\"Building Pose-derived ROIs for {self.roi_type} [{self.split}]...\")\n",
"        PPE_CLASSES = {\n",
"            'helmet': {'present': 0, 'absent': 7},\n",
"            'vest':   {'present': 2, 'absent': None},\n",
"            'gloves': {'present': 1, 'absent': 9},\n",
"            'boots':  {'present': 3, 'absent': 10},\n",
"            'goggles':{'present': 4, 'absent': 8}\n",
"        }\n",
"        \n",
"        c_map = PPE_CLASSES.get(self.roi_type, {})\n",
"        present_cls = c_map.get('present')\n",
"        absent_cls = c_map.get('absent')\n",
"        \n",
"        img_dir = self.dataset_dir / 'images' / self.split\n",
"        lbl_dir = self.dataset_dir / 'labels' / self.split\n",
"        \n",
"        for img_path in list(img_dir.glob('*.jpg')):\n",
"            lbl_path = lbl_dir / img_path.with_suffix('.txt').name\n",
"            if not lbl_path.exists(): continue\n",
"            \n",
"            img = cv2.imread(str(img_path))\n",
"            if img is None: continue\n",
"            h, w = img.shape[:2]\n",
"            \n",
"            persons, present_boxes, absent_boxes = [], [], []\n",
"            for line in lbl_path.read_text().splitlines():\n",
"                parts = line.strip().split()\n",
"                if len(parts) < 5: continue\n",
"                cls_id = int(parts[0])\n",
"                cx, cy, bw, bh = (float(p) for p in parts[1:5])\n",
"                x1, y1 = (cx - bw/2)*w, (cy - bh/2)*h\n",
"                x2, y2 = (cx + bw/2)*w, (cy + bh/2)*h\n",
"                \n",
"                if cls_id == 6: persons.append(BBox(x1, y1, x2, y2))\n",
"                elif cls_id == present_cls: present_boxes.append(BBox(x1, y1, x2, y2))\n",
"                elif cls_id == absent_cls: absent_boxes.append(BBox(x1, y1, x2, y2))\n",
"                \n",
"            for pbox in persons:\n",
"                px1, py1, px2, py2 = int(max(0, pbox.x1)), int(max(0, pbox.y1)), int(min(w, pbox.x2)), int(min(h, pbox.y2))\n",
"                worker_crop = img[py1:py2, px1:px2]\n",
"                if worker_crop.size == 0: continue\n",
"                \n",
"                res = self.pose_model.predict(worker_crop, verbose=False, device='cpu')[0]\n",
"                if res.keypoints is None or len(res.keypoints.data) == 0: continue\n",
"                kp = res.keypoints.data[0].cpu().numpy()\n",
"                \n",
"                target_roi = None\n",
"                \n",
"                if self.roi_type == 'helmet':\n",
"                    head_kps = [k for k in kp[:5] if k[2] > 0.5]\n",
"                    if head_kps:\n",
"                        cx = np.mean([k[0] for k in head_kps]) + px1\n",
"                        cy = np.mean([k[1] for k in head_kps]) + py1\n",
"                        head_size = 40.0\n",
"                        if kp[5][2] > 0.5 and kp[6][2] > 0.5:\n",
"                            shoulder_width = abs(kp[6][0] - kp[5][0])\n",
"                            head_size = max(30.0, shoulder_width * 0.45)\n",
"                        \n",
"                        rx1 = cx - head_size * 1.4\n",
"                        rx2 = cx + head_size * 1.4\n",
"                        ry1 = cy - head_size * 2.2\n",
"                        ry2 = cy + head_size * 1.0\n",
"                        target_roi = BBox(rx1, ry1, rx2, ry2)\n",
"                        \n",
"                elif self.roi_type == 'vest':\n",
"                    l_sh, r_sh = kp[5], kp[6]\n",
"                    l_hip, r_hip = kp[11], kp[12]\n",
"                    if all(k[2] > 0.5 for k in [l_sh, r_sh, l_hip, r_hip]):\n",
"                        pad = 10\n",
"                        rx1 = min(l_sh[0], r_sh[0], l_hip[0], r_hip[0]) + px1 - pad\n",
"                        ry1 = min(l_sh[1], r_sh[1]) + py1 - pad\n",
"                        rx2 = max(l_sh[0], r_sh[0], l_hip[0], r_hip[0]) + px1 + pad\n",
"                        ry2 = max(l_hip[1], r_hip[1]) + py1 + pad\n",
"                        target_roi = BBox(rx1, ry1, rx2, ry2)\n",
"                        \n",
"                elif self.roi_type == 'gloves':\n",
"                    # Pick the highest confidence wrist\n",
"                    w_kp = kp[9] if kp[9][2] > kp[10][2] else kp[10]\n",
"                    if w_kp[2] > 0.5:\n",
"                        r = 30.0\n",
"                        target_roi = BBox(w_kp[0]+px1-r, w_kp[1]+py1-r, w_kp[0]+px1+r, w_kp[1]+py1+r)\n",
"                        \n",
"                elif self.roi_type == 'boots':\n",
"                    # Pick highest confidence ankle\n",
"                    a_kp = kp[15] if kp[15][2] > kp[16][2] else kp[16]\n",
"                    if a_kp[2] > 0.5:\n",
"                        bw, bhu, bhd = 35.0, 45.0, 20.0\n",
"                        target_roi = BBox(a_kp[0]+px1-bw, a_kp[1]+py1-bhu, a_kp[0]+px1+bw, a_kp[1]+py1+bhd)\n",
"                        \n",
"                elif self.roi_type == 'goggles':\n",
"                    eyes = [k for k in (kp[1], kp[2], kp[0]) if k[2] > 0.5]\n",
"                    if len(eyes) >= 1:\n",
"                        cx_eye = sum(k[0] for k in eyes) / len(eyes) + px1\n",
"                        cy_eye = sum(k[1] for k in eyes) / len(eyes) + py1\n",
"                        g_size = max(15.0, 24.0)\n",
"                        target_roi = BBox(cx_eye-g_size*1.5, cy_eye-g_size*0.8, cx_eye+g_size*1.5, cy_eye+g_size*1.2)\n",
"                        \n",
"                if target_roi is None: continue\n",
"                \n",
"                label = None\n",
"                for gt in present_boxes:\n",
"                    if compute_iou(target_roi, gt) > 0.05:\n",
"                        label = 2; break\n",
"                if label is None:\n",
"                    for gt in absent_boxes:\n",
"                        if compute_iou(target_roi, gt) > 0.05:\n",
"                            label = 0; break\n",
"                            \n",
"                if label is None and absent_cls is None:\n",
"                    label = 0  # Default to absent for vest/harness since no bounding box exists\n",
"                elif label is None:\n",
"                    continue\n",
"                \n",
"                tx1, ty1 = int(max(0, target_roi.x1)), int(max(0, target_roi.y1))\n",
"                tx2, ty2 = int(min(w, target_roi.x2)), int(min(h, target_roi.y2))\n",
"                \n",
"                roi_img = img[ty1:ty2, tx1:tx2]\n",
"                if roi_img.size > 0:\n",
"                    self.samples.append((roi_img, label))\n",
"                    \n",
"        print(f\"  → Extracted {len(self.samples)} valid samples for {self.roi_type}.\")\n",
"        \n",
"    def __len__(self): return len(self.samples)\n",
"    def __getitem__(self, idx):\n",
"        return self.samples[idx][0], self.samples[idx][1]"
])

